function Answer1() {
  if (document.getElementById("answer1").style.display === "none") {
    document.getElementById("answer1").style.display = "block";
    document.getElementById("svg1").style.transform = "rotate(180deg)";
    document.getElementById("question1").style.fontWeight = "700";
  } else {
    document.getElementById("answer1").style.display = "none";
    document.getElementById("svg1").style.transform = "rotate(0deg)";
    document.getElementById("question1").style.fontWeight = "400";
  }
}

